public class Operators_And_Literals {
    public static void main(String[] args) {

        // OPERATORS

        int a = 10;
        int b = 2;
        int c = 1;
        c = a + b;   // '+' is operator, 'a' & 'b' are operands
        c++;         // operation can be done on single operand or more
        System.out.println(c);

        // LITERALS

        String name = "Rohit"; // here 'Rohit' is literal (String literal)
        int num = 5;           // Integer '5' is Integer literal

        //KEYWORDS

        // int public = 10; 'public' is reserved keyword among 50 keyword ( reserved words ) in Java
    }
}
